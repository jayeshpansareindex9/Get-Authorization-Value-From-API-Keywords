class accessToken {
	/**
	 * Created By: jayesh
	 * Date: 11-10-2018
	 * Description:  get session from login API
	 * **/
	@Keyword
	def getAccessTokenFromAPI(def accessTokenKey, ResponseObject APIResult) {
		def slurper = new JsonSlurper()
		if(APIResult){
			Map  APIResponce = slurper.parseText(APIResult.getResponseBodyContent())
			boolean isKeyPresent = APIResponce.get(accessTokenKey)
			if (isKeyPresent) {
				return APIResponce.get(accessTokenKey)
			}else{
				return "Key is not present"
			}
		}else{
			return "Error: Parsing error.";
		}
	}
}